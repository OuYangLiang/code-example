package com.personal.test.demo.web;

public class People {
    private String name;
    private Integer age;

    /**
     * @return name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name
     */
    public void setName(final String name) {
        this.name = name;
    }

    /**
     * @return age
     */
    public Integer getAge() {
        return age;
    }

    /**
     * @param age
     */
    public void setAge(final Integer age) {
        this.age = age;
    }

}
